const items = [
  { name: 'Футболка', price: 250, quantity: 2 },
  { name: 'Джинси', price: 800, quantity: 1 },
  { name: 'Кросівки', price: 1200, quantity: 1 }
];
function calculateTotalCost(items) {
  let total = 0;
  for (const item of items) {
    total += item.price * item.quantity;
  }
  return total;
}

